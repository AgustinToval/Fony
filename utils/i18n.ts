    export const t = (key: string, lang: string): string => {
    const parts = key.split('.');
    let result: any = traducciones[lang];

    for (const part of parts) {
        result = result?.[part];
        if (typeof result === 'undefined') return key;
    }

    return typeof result === 'string' ? result : key;
    };
